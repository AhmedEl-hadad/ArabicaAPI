def build_restaurant_dataset(names, category_label, calories_range, price_range, image_prefix):
    calories_values = distribute_ints(calories_range[0], calories_range[1], len(names))
    price_values = distribute_ints(price_range[0], price_range[1], len(names))
    entries = []
    for idx, name in enumerate(names, start=1):
        entries.append(
            {
                "id": f"menu-{idx:03d}",
                "name": name,
                "category": category_label,
                "calories": calories_values[idx - 1],
                "price": price_values[idx - 1],
                "image": f"/images/restaurants/{image_prefix}/menu-{idx:03d}.jpg",
            }
        )
    return entries


